void DrawChar(UINT32 *fb, UINT32 pitch, int x, int y, char c, UINT32 color) {
    if (c < 0 || c > 127) return;
    for (int row = 0; row < 8; row++) {
        unsigned char line = font[(int)c][row];
        for (int col = 0; col < 8; col++) {
            if (line & (1 << (7 - col))) fb[(y + row) * pitch + (x + col)] = color;
        }
    }
}

void DrawString(UINT32 *fb, UINT32 pitch, int x, int y, const char *str, UINT32 color) {
    if (!fb) return;
    int cur_x = x;
    while (*str) { DrawChar(fb, pitch, cur_x, y, *str, color); cur_x += 8; str++; }
}

void DrawHex64(UINT32 *fb, UINT32 pitch, int x, int y, UINT64 value, UINT32 color) {
    char hex_chars[] = "0123456789ABCDEF"; char buffer[19]; 
    buffer[0] = '0'; buffer[1] = 'x'; buffer[18] = '\0';
    for (int i = 17; i >= 2; i--) { buffer[i] = hex_chars[value & 0x0F]; value >>= 4; }
    DrawString(fb, pitch, x, y, buffer, color);
}

void EraseCharBlock(UINT32 *fb, UINT32 pitch, int x, int y) {
    for (int row = 0; row < 8; row++) {
        for (int col = 0; col < 8; col++) fb[(y + row) * pitch + (x + col)] = 0x00000000;
    }
}

void ScrollScreen() {
    for (UINT32 y = 8; y < GlobalHeight; y++) {
        for (UINT32 x = 0; x < GlobalWidth; x++) {
            GlobalFrameBuffer[(y - 8) * GlobalPitch + x] = GlobalFrameBuffer[y * GlobalPitch + x];
        }
    }
    for (UINT32 y = GlobalHeight - 8; y < GlobalHeight; y++) {
        for (UINT32 x = 0; x < GlobalWidth; x++) {
            GlobalFrameBuffer[y * GlobalPitch + x] = 0x00000000;
        }
    }
}

void UpdateCursor() {
    if (CursorX >= GlobalWidth) { CursorX = 0; CursorY += 8; }
    if (CursorY >= GlobalHeight) { ScrollScreen(); CursorY -= 8; }
}

void ClearScreen() {
    for (UINTN i = 0; i < (GlobalPitch * GlobalHeight); i++) GlobalFrameBuffer[i] = 0x00000000; 
    CursorX = 0; CursorY = 0;
}

void SaveMouseBg() {
    for (int i = 0; i < 5; i++) {
        for (int j = 0; j < 5; j++) {
            if (PrevMouseY + i < GlobalHeight && PrevMouseX + j < GlobalWidth)
                MouseBg[i][j] = GlobalFrameBuffer[(PrevMouseY + i) * GlobalPitch + (PrevMouseX + j)];
        }
    }
}

void RestoreMouseBg() {
    for (int i = 0; i < 5; i++) {
        for (int j = 0; j < 5; j++) {
            if (PrevMouseY + i < GlobalHeight && PrevMouseX + j < GlobalWidth)
                GlobalFrameBuffer[(PrevMouseY + i) * GlobalPitch + (PrevMouseX + j)] = MouseBg[i][j];
        }
    }
}

void DrawMouseCursor() {
    for (int i = 0; i < 5; i++) {
        for (int j = 0; j < 5; j++) {
            if (MouseY + i < GlobalHeight && MouseX + j < GlobalWidth)
                GlobalFrameBuffer[(MouseY + i) * GlobalPitch + (MouseX + j)] = 0x00FF0000; 
        }
    }
}